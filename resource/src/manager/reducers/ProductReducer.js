export const productReducer = (state=products,action)=>{
    switch (action.type) {
        case 'ADD_PRODUCT':
            return [...state,action.product];

        case 'DELETE_PRODUCT':
            return state.filter(item=>item.id !== action.id)

        case 'EDIT_PRODUCT':
            return state.map(i=>{
                if (i.id === action.id) {
                    return {
                        ...i,...action.update
                    }
                }else{
                    return i;
                }
            })
            case 'PRODUCT_DETAILS':
                return state.map(i=>{
                    if (i.id === action.id) {
                        return {
                            ...i,...action.update
                        }
                    }else{
                        return i;
                    }
                })


            case "GET_PRODUCTS":
                return action.products;
            
    
        default:
            return state;
    }
}




